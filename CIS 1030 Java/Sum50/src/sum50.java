
public final class sum50 {

	public static void main(String[] args) {
		
		// Variables
		int countingNum = 1;
		int runningTotal = 0;
		
		//Loop
		for (countingNum = 1; countingNum <= 50; countingNum++) {
			runningTotal = runningTotal + countingNum;
			//You didn't specify you wanted output but that seemed like a good idea to me
			System.out.println("Running Total: " + runningTotal);
		}
		
		
		

	}

}
