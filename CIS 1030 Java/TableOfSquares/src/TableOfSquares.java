public final class TableOfSquares {

	public static void main(String[] args) {
		// Varibles
		int countingNumber = 0;
		int squaredNumber = 0;
		
		//Baby loop
		for (countingNumber = 0; countingNumber <= 20; countingNumber++) {
			System.out.println("Integer: " + countingNumber);
			squaredNumber = (countingNumber * countingNumber);
			System.out.println("Squared: " + (squaredNumber));
	}

}
}
